const updateUserAvatar = asyncHandler(async (req, res) => {
  const avatarLocalPath = req.file?.path;

  if (!avatarLocalPath) {
    throw new ApiError(400, "Avatar file is missing");
  }

  const avatar = await uploadOnCloudinary(avatarLocalPath);

  if (!avatar) {
    throw new ApiError(400, "Error while uploading avatar");
  }

  const oldAvatarUrl = await User.findById(req.user?._id).select("avatar");

  const user = await User.findByIdAndUpdate(
    req.user?._id,
    {
      $set: { avatar: avatar.url },
    },
    { new: true }
  ).select("-password");

  if (oldAvatarUrl && oldAvatarUrl.avatar) {
    await deleteFromCloudinary(oldAvatarUrl.avatar);
  }

  return res
    .status(200)
    .json(new ApiResponse(200, user, "The avatar is updated successfully"));
});

